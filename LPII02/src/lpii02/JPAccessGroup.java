    private void jpAccessGroupHierarchyChanged(java.awt.event.HierarchyEvent evt) {//GEN-FIRST:event_jpAccessGroupHierarchyChanged
        if (this._loadPassed) {
            return;
        }

        try {
            this.loadTable();
            this._loadPassed = true;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    "Ocorreu um erro ao carregar os grupos, tente novamente.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jpAccessGroupHierarchyChanged

