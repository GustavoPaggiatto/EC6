    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            txtName.setText(txtName.getText().trim());
            txtDescription.setText(txtDescription.getText().trim());

            AccessGroup aGroup = this._agroupBusiness.getInstance();

            aGroup.setName(txtName.getText());
            aGroup.setDescription(txtDescription.getText());

            this._agroupBusiness.insert(aGroup);

            //reload table...
            this.loadTable();

            //reset...
            this.clearControls();

            //message success...
            JOptionPane.showMessageDialog(
                    null,
                    "Cadastro efetuado com sucesso!",
                    "Info.",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

