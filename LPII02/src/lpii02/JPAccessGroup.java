    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if (JOptionPane.showConfirmDialog(
                    this,
                    "Deseja realmente excluir o Grupo de Acesso?",
                    "Confirmação",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE) == 0) {

                //deleting model...
                AccessGroup model = this._agroupBusiness.getByName(txtName.getText().trim());

                if (model != null) {
                    this._agroupBusiness.delete(model);
                }

                //reset...
                this.clearControls();

                //load table...
                this.loadTable();

                //message success...
                JOptionPane.showMessageDialog(
                        null,
                        "Exclusão efetuada com sucesso!",
                        "Info.",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnFirstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFirstActionPerformed
        if (this._page == 1) {
            return;
        }

        this._page = 1;

        try {
            this.loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnFirstActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        if (this._page == 1) {
            return;
        }

        this._page--;

        try {
            this.loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        List<AccessGroup> ags = this._agroupBusiness.get();
        int maxPage = (int) Math.round(ags.size() / Double.parseDouble(this._qtdPerPage + ""));

        if (this._page >= maxPage) {
            return;
        }

        this._page++;

        try {
            this.loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnLastActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLastActionPerformed
        try {
            List<AccessGroup> grs = this._agroupBusiness.get();

            this._page = (int) Math.round(grs.size() / Double.parseDouble(this._qtdPerPage + ""));
            this.loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLastActionPerformed

    private void loadTable() {
        List<AccessGroup> aGroups = this._agroupBusiness.get();

        if (aGroups.size() > this._qtdPerPage) {
            aGroups = aGroups.subList((this._page - 1) * this._qtdPerPage, this._qtdPerPage);
        }

        if (aGroups != null) {
            DefaultTableModel dtm = (DefaultTableModel) this.grGroups.getModel();

            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }

            for (AccessGroup ag : aGroups) {
                dtm.addRow(new Object[]{
                    ag.getId(),
                    ag.getName(),
                    ag.getDescription()
                });
            }
        }
    }

    private void clearControls() {
        this.txtName.setText("");
        this.txtDescription.setText("");
    }

    private void valueRowChanged(ListSelectionEvent e) {
        if (this.grGroups.getSelectedRows() == null
                || this.grGroups.getSelectedRows().length == 0) {
            return;
        }

        int selectedRow = this.grGroups.getSelectedRows()[0];

        this.txtName.setText(this.grGroups.getValueAt(selectedRow, 1).toString());
        this.txtDescription.setText(this.grGroups.getValueAt(selectedRow, 2).toString());
    }

